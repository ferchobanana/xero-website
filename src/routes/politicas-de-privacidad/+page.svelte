<div class="politicas">

    <h1>Políticas de privacidad</h1>

    <div class="seccion">
        <h2>1. Introducción</h2>
        <p class="texto">
           Bienvenido a XeroWeb. Nos comprometemos a proteger y respetar su privacidad. Esta Política de Privacidad explica cómo recopilamos, utilizamos, compartimos y protegemos su información personal cuando utiliza nuestra plataforma de creación de sitios web y herramientas de ventas en línea. 
        </p>
    </div>

    <div class="seccion">
        <h2>2. Información que Recopilamos</h2>
        <p class="texto">
            Podemos recopilar y procesar los siguientes datos sobre usted:
        </p>
        <ul class="lista">
            <li><b>Información de Registro:</b> nombre, dirección de correo electrónico, número de teléfono, dirección postal.</li>    
            <li><b>Información de Pago:</b> detalles de la tarjeta de crédito u otra información de pago.</li>
            <li><b>Información de Uso:</b> datos sobre su interacción con nuestra plataforma, incluyendo direcciones IP, tipo de navegador, páginas visitadas y tiempos de acceso.</li>
            <li><b>Información de Comunicación:</b> registros de cualquier correspondencia con nosotros.</li>
            <li><b>Información de Terceros:</b> datos que obtenemos de terceros para mejorar nuestros servicios, como integraciones con redes sociales y proveedores de pago.</li>
        </ul>
    </div>

    <div class="seccion">
        <h2>3. Uso de la información</h2>
        <p>Utilizamos la información que recopilamos para:</p>
        <ul>
            <li>Proveer, operar y mantener nuestra plataforma.</li>
            <li>Procesar transacciones y enviar confirmaciones</li>
            <li>Mejorar, personalizar y expandir nuestros servicios</li>
            <li>Comunicarnos con usted, incluyendo el envío de actualizaciones, ofertas y promociones.</li>
            <li>Proteger contra el fraude y garantizar la seguridad de nuestra plataforma.</li>
            <li>Cumplir con obligaciones legales y resolver disputas</li>
        </ul>
    </div>

    <div class="seccion">
        <h2>4. Compartir y Divulgar Información</h2>
        <p>No compartimos su información personal con terceros, salvo en las siguientes circunstancias:</p>
        <ul>
             <li><b>Proveedores de Servicios:</b> Compartimos información con proveedores de servicios que realizan funciones y servicios en nuestro nombre, como procesamiento de pagos, análisis de datos, y soporte al cliente.</li>
            <li><b>Requerimientos Legales:</b> Podemos divulgar su información para cumplir con leyes aplicables, responder a solicitudes gubernamentales, y proteger nuestros derechos legales.</li>
            <li><b>Transacciones Comerciales:</b>En caso de fusión, adquisición o venta de la totalidad o parte de nuestros activos, su información puede ser transferida como parte de esa transacción.</li>
        </ul>
    </div>

    <div class="seccion">
        <h2>5. Seguridad de la Información</h2>
        <p>Implementamos medidas de seguridad razonables para proteger su información personal de accesos no autorizados, alteraciones, divulgaciones o destrucciones. Sin embargo, ninguna transmisión por Internet o método de almacenamiento electrónico es 100% seguro, por lo que no podemos garantizar una seguridad absoluta.</p>
    </div>

    <div class="seccion">
        <h2>6. Retención de Información</h2>
        <p>Retenemos su información personal durante el tiempo que sea necesario para cumplir con los fines para los cuales fue recopilada, incluyendo cumplir con obligaciones legales, resolver disputas y hacer cumplir nuestros acuerdos.</p>
    </div>

    <div class="seccion">
        <h2>7. Sus Derechos</h2>
        <p>Dependiendo de su ubicación, puede tener ciertos derechos en relación con su información personal, incluyendo el derecho a acceder, corregir, eliminar o restringir el uso de su información personal. Para ejercer estos derechos, por favor contáctenos en soporte@xeroweb.net</p>
    </div>

    <div class="seccion">
        <h2>8. Cookies y Tecnologías Similares</h2>
        <p>Utilizamos cookies y tecnologías similares para mejorar su experiencia en nuestra plataforma. Puede controlar el uso de cookies a través de la configuración de su navegador.</p>
    </div>

    <div class="seccion">
        <h2>9. Cambios a esta Política de Privacidad</h2>
        <p>Podemos actualizar esta Política de Privacidad de vez en cuando. Le notificaremos sobre cualquier cambio mediante la publicación de la nueva política en esta página. Le recomendamos revisar esta política periódicamente para estar al tanto de cualquier cambio.</p>
    </div>

    <div class="seccion">
        <h2>10. Contacto</h2>
        <p>Si tiene alguna pregunta o inquietud sobre esta Política de Privacidad, no dude en contactarnos en soporte@xeroweb.net</p>
    </div>

